        <h2>The Process</h2>
        
        <p>
            The design process for this website was a bit of a challenge for me. The client came to me with a logo already designed and used in other business materials. They knew they wanted a website but did not yet have a vision for the design. I was given a blank slate to work with (both incredibly freeing and incredibly daunting â˜¯).
        </p>

        <p>
            I tinkered with several different designs, all of my initial prototypes were very <span class="italic">"designed"</span>. They featured custom fonts, full width hero images, and lots of color. 
        </p>

        <p>
            I really liked the designs (big suprise ðŸ˜‰) but I really struggled to fit the client's logo into my "designs". The logo itself is stunning but was designed by a print designer with the purpose of printing business cards. It features some very intricate text and floral elements - elements that make the traditional top left logo treatment impractical.
        </p>

        <img src="{{ asset('img/katyrosefloral-logo.png') }}" alt="Katy Rose Floral Logo" class="max-w-md mx-auto">

        <p>
            After showing intial designs to the client we were able to refine a vision for a clean and simple design. With this feedback in hand, I went back to the drawing board (or <a href="https://excalidraw.com/" target="_blank" rel="noopener">excalidraw</a> to be specific).
        </p>

        <img src="{{ asset('img/katyrosefloral-wireframe.jpeg') }}" alt="Wireframe design for the final katyrosefloral.com website">

        <p>
            As you can seen in the screenshot above, in this design instead of trying to fit the logo into my design, I made the logo the centerpiece of the design (which was the right solution to begin with ðŸ˜‚). I kept the design very clean and minimal - and TBH it was my favorite design by far. The client loved it so I built and shipped it, you can check it out at: <a href="https://katyrosefloral.com" target="_blank" rel="noopener">https://katyrosefloral.com</a>.
